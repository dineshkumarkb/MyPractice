lst = [1,2,3,4,5]

# Clear a list in python 3
lst.clear()
print(f" The cleared list object is {lst} ")

lst = [1,2,3,4,5]

# Clear a list in python 2
del lst[:]
print(f" The python 2 cleared list is {lst} ")

lst = [1,2,3,4,5]

# Replace all elements in a list
lst[:] = [7,8,9]
print(f" The replaced list is {lst}")

